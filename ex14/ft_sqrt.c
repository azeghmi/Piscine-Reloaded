int	ft_sqrt(int nb)
{
	int i;
	int res;

	i = 1;
	res = 0;
	if (nb < 0)
		return (res);
	while (i < nb / i)
	{
		i++;
		if (i * i == nb)
			res = i;
	}
	return (res);
}
